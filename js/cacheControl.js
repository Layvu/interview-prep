/*
POST-запросы по умолчанию не кэшируются согласно спецификации HTTP, так как они считаются небезопасными и изменяющими состояние
Чтобы гарантировать отсутствие кэширования, сервер должен отправлять заголовки:
*/
({
    'Cache-Control': 'no-store, no-cache, must-revalidate',
    Pragma: 'no-cache',
    Expires: '0',
});
